    private void Equal1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Equal1ActionPerformed
        if (darbiba){
            int skaitlis1 = Integer.valueOf(pirmSkaitlis);
            int skaitlis2 = Integer.valueOf(Text.getText());
            Aprekini rekins = new Aprekini();
            
            Text.setText(rekins.Aprekinat(skaitlis1, skaitlis2, zime));
            darbiba = false;
            zime = "";
        }
        
    }//GEN-LAST:event_Equal1ActionPerformed

