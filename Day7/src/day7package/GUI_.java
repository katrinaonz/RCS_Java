private void SkaitluParadisana(String poga){
              
       String currenctText = Text.getText();
       
       if (currenctText.equals("0") ||currenctText.equals("*") || currenctText.equals("+")||currenctText.equals("-")
               ||currenctText.equals("/")){
           currenctText = "";
       }
        Text.setText(currenctText + poga);
    }
